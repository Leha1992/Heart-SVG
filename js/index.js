let i = 0;

function renderCircle () {
	let circle = paper.circle(coord[i].x,coord[i].y, 0).attr({
		fill: '#fff'
	})

	circle.animate({r: Math.floor(Math.random() *(5-2) + 2 )},50,mina.bounce);
}




setTimeout(function renderLine () {
		
	let way = paper.path(`M ${coord[i].x}, ${coord[i].y}`).attr({
		fill: 'transparent',
		stroke: '#fff',
		strokeWidth: 1
	}).animate({d:`M ${coord[i].x}, ${coord[i].y} L ${coord[i+1].x}, ${coord[i+1].y}`} ,100, mina.linear,renderCircle);
	
	way.animate({stroke: '#a300a9'},2000, mina.linear);
	
	i++
	
	let time =setTimeout(renderLine,140);	
	
	if(i === coord.length-1) {
		clearTimeout(time);
   	}	
},140)






 


